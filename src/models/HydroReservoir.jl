"""
    mutable struct HydroReservoir <: Component
        name::String
        available::Bool
        initial_volume::Union{Float64}
        storage_volume_limits::Union{Nothing, MinMax, TimeSeriesKey}
        spillage_limits::Union{Nothing, MinMax, TimeSeriesKey}
